public class ClothingDTO {
    private String name;
    private String size;
    private String color;
    private double price;

    public String getName() {
        return name;
    }

    public String getSize() {
        return size;
    }

    public String getColor() {
        return color;
    }

    public double getPrice() {
        return price;
    }
}
